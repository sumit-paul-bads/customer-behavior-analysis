-- Q11. What are the top 3 most purchased products within each category ?

select * from customer_data;

select item_purchased, count(customer_id) as count_of_purchases, round(avg(purchase_amount),2) as avg_revenue, round(sum(purchase_amount),2) as total_revenue
from customer_data
group by item_purchased
order by count_of_purchases desc limit 3;

with cte as
(
	select category, item_purchased, count(item_purchased) as purchase_count,
    rank() over(partition by category order by count(item_purchased) desc) as rank_given
from customer_data
group by category, item_purchased
)
select * from cte
where rank_given <=3;

-- Shirt, shoes and Jewlry are the top 3 products being sold and are the most revenue generating products but
-- within each category we see the finer details

-- Impact:
-- 			1. Improves product placement and recommendations
-- 			2. Inventory optimization can be done
-- 			3. Increase sales through best sellers

