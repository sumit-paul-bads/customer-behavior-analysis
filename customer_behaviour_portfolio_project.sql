-- Q10. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe ?

select * from customer_data;

select min(previous_purchases), max(previous_purchases), avg(previous_purchases)
from customer_data;

SELECT 
    CASE
        WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Non Repeat Buyers'
    END AS repeatability,
    COUNT(*) AS total_customers,
    SUM(CASE
        WHEN subscription_status = 'Yes' THEN 1
        ELSE 0
    END) AS subscribers,
    round((SUM(CASE
        WHEN subscription_status = 'Yes' THEN 1
        ELSE 0
    END) * 100) / COUNT(*),2) AS subscription_rate
FROM
    customer_data
GROUP BY repeatability;

-- Repeat Buyers are more but their subscription rate is low probably due to some issues with quality

-- Impact:
-- 			1. Proper segmentation of churn data
-- 			2. Increases retention rate
-- 			3. Targetted attention can be given

